package class04_IfElseSwitch_Example;

public class QuizNine {

	public static void main(String[] args) {
		int i=2;
		switch(i)
		{
		case 1:
			System.out.println("Case1");
		case 2:
			System.out.println("Case2");
		case 3:
			System.out.println("Case3");
		default:
			System.out.println("Default");
	}//switch

}//main
}//class