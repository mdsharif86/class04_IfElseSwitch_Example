package class04_IfElseSwitch_Example;

public class QuizEleven {

	public static void main(String[] args) {
		char ch='b';
		switch(ch)
		{
		case 'd':
			System.out.println("case1");
			break;
		case 'b':
			System.out.println("Case2");
			break;
		case 'x':
			System.out.println("Case3");
			break;
		case 'y':
			System.out.println("Case3");
			break;
		default:
			System.out.println("Default");
		
		}
	}//main

}//class
