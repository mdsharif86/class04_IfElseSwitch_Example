package class04_IfElseSwitch_Example;

public class QuizTwety {

	public static void main(String[] args) {
		int dayValue;
		dayValue = 2;
		switch(dayValue) {
		case 1:
			System.out.println("Life is great");
			break;
		case 2:
			System.out.println("Life is Awesome");
			break;
			default:
				System.out.println("Life can be hard sometime");
		}
			

}//main
}//class